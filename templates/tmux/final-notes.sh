# Tmux 安装完成后的提示
cat <<'TMUX_FINAL_NOTES'

============================================================
 Tmux 配置流程已结束
------------------------------------------------------------
 1) 如果安装了 JetBrainsMono Nerd Font，请在终端设置中选择：
      JetBrainsMono Nerd Font Mono
    否则状态栏图标可能显示为方框。

 2) 启动 tmux：
      tmux

 3) 如果 TPM 插件未全部安装成功，可在 tmux 内按前缀键后再按 Shift+I 重试。

 4) 修改 ~/.tmux.conf 后，可在 tmux 内按前缀键后再按 r 重载配置。
============================================================
TMUX_FINAL_NOTES

log_ok "Tmux 后续操作提示已显示"
